// Entry point
// ---------------------------------------------------------------------------

/// Generate a Rust project from a type-checked Assura file.
///
/// Walks the AST, maps Assura declarations to Rust source code, and
/// formats the result with `prettyplease`. Returns a `GeneratedProject`
/// with a `Cargo.toml` and generated `.rs` files.
pub fn codegen_with_config(typed: &TypedFile, config: &BackendConfig) -> GeneratedProject {
    let _ = config; // T119: backend selection will influence Cargo.toml and build flags
    codegen(typed)
}

/// Generate a Rust project from a type-checked Assura file.
///
/// Uses default backend configuration (`Rustc`, opt-level 2, no debug info).
/// For custom configuration, use [`codegen_with_config`].
pub fn codegen(typed: &TypedFile) -> GeneratedProject {
    let source = &typed.resolved.source;

    let project_name = source
        .project
        .as_ref()
        .map(|p| p.name.clone())
        .unwrap_or_else(|| "generated".to_string());

    // Sanitize project name for Cargo (replace non-alphanumeric with _)
    let crate_name: String = project_name
        .chars()
        .map(|c| {
            if c.is_alphanumeric() || c == '_' || c == '-' {
                c
            } else {
                '_'
            }
        })
        .collect();

    let cargo_toml = generate_cargo_toml(&crate_name);

    let mut code = String::new();

    // Inner module attribute (prettyplease preserves attributes)
    code.push_str("#![allow(dead_code, unused_variables)]\n\n");

    // Collect all generated items
    for decl in &source.decls {
        match &decl.node {
            Decl::TypeDef(t) => generate_type_def(t, &mut code),
            Decl::EnumDef(e) => generate_enum_def(e, &mut code),
            Decl::Contract(c) => generate_contract(c, &mut code),
            Decl::Extern(ex) => generate_extern(ex, &mut code),
            Decl::FnDef(f) => {
                // T043 CORE.1: ghost functions are erased at runtime
                // T044 CORE.2: lemma functions are erased at runtime
                if !f.is_ghost && !f.is_lemma {
                    generate_fn_def(f, &mut code);
                }
            }
            Decl::Service(s) => generate_service(s, &mut code),
            Decl::Block { kind, name, body } => {
                generate_block(kind, name, body, &mut code);
            }
        }
    }

    // Format generated Rust via prettyplease, then prepend header comment
    let formatted_body = format_rust(&code);
    let formatted = format!(
        "// Generated by the Assura compiler.\n// Do not edit manually.\n\n{formatted_body}"
    );

    GeneratedProject {
        cargo_toml,
        files: vec![("src/lib.rs".to_string(), formatted)],
    }
}

// ---------------------------------------------------------------------------
// Cargo.toml generation
